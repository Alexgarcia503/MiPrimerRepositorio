# 1. Bloque de texto de 3 líneas
texto = """Amanecer en el valle
la brisa acaricia el alma
canta la naturaleza"""

# 2. Contar cuántas veces aparece la letra "a"
cantidad_a = texto.count("a")

# 3. Dividir el texto por saltos de línea
lista_oraciones = texto.splitlines()

# Mostrar resultados
print("Cantidad de 'a':", cantidad_a)
print("Lista de oraciones:", lista_oraciones)
